export function getCategoryColor(category: string) {
  const customColors: Record<string, string> = {
    'Finance': '#10B981',
    'Executive': '#8B5CF6',
    'Sales': '#F59E0B',
    'Marketing': '#EC4899',
    'Operations': '#3B82F6',
    'Research': '#6366F1',
    'Legal/Compliance': '#EF4444',
    'Creator': '#F97316',
  };
  return customColors[category] || '#64748B';
}
